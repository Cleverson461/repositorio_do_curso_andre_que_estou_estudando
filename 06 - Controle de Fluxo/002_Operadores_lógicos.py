# Logical Operators - (Operadores Logicos)

renda_acima_5mil = True
nome_limpo = False

# AND (E) 
if renda_acima_5mil and nome_limpo:
    print("Financiamento Aprovado.")
else:
    print("Financiamento Negado.")

print('===============================')

# OR (OU)
if renda_acima_5mil or nome_limpo:
    print("Financiamento Aprovado.")
else:
    print("Financiamento Negado.")